package objects;

public class File 
{
	
	private int id;
	private String name;
	private String link;
	
	private String curseName; 
	private String fileExtension;
	
	
	public File(int id, String name, String link, String curseName, String fileExtension) {
		super();
		this.id = id;
		this.name = name;
		this.link = link;
		this.curseName = curseName;
		this.fileExtension = fileExtension;
	}
	
	
	
	
	

}
